// Category configuration
const CATEGORIES = [
  {
    key: 'basic',
    label: 'Basic',
    types: ['url', 'text', 'wifi', 'vcard', 'email', 'phone', 'sms'] as QRContentType[],
  },
  {
    key: 'social',
    label: 'Social',
    types: ['whatsapp', 'facebook', 'instagram', 'apps'] as QRContentType[],
  },
  {
    key: 'reviews',
    label: 'Reviews',
    types: ['google-review'] as QRContentType[],
  },
  {
    key: 'media',
    label: 'Media',
    types: ['pdf', 'images', 'video', 'mp3'] as QRContentType[],
    proOnly: true,
  },
  {
    key: 'landing',
    label: 'Landing Pages',
    types: ['menu', 'business', 'links', 'coupon', 'social'] as QRContentType[],
    proOnly: true,
  },
];

export function QRTypeSelector({
  value,
  onChange,
  userTier = 'free',
  showProTypes = true,
}: QRTypeSelectorProps) {
  const isPro = userTier === 'pro' || userTier === 'business';

  // Filter categories based on showProTypes
  const visibleCategories = showProTypes
    ? CATEGORIES
    : CATEGORIES.filter(cat => !cat.proOnly);

  return (
    <div className="space-y-6">
      {visibleCategories.map((category) => (
        <div key={category.key}>
          <h3 className="text-sm font-medium text-muted-foreground mb-3 flex items-center gap-2">
            {category.label}
            {category.proOnly && (
              <span className="text-[10px] bg-primary/20 text-primary px-2 py-0.5 rounded-full">
                Pro
              </span>
            )}
          </h3>
          <div className="flex flex-wrap gap-2">
            {category.types.map((type) => {
              const config = TYPE_CONFIG[type];
              const isProType = PRO_ONLY_TYPES.includes(type);
              const isDisabled = isProType && !isPro;

              return (
                <button
                  key={type}
                  onClick={() => !isDisabled && onChange(type)}
                  disabled={isDisabled}
                  className={cn(
                    'flex items-center gap-2 px-4 py-2 rounded-lg transition-all duration-200',
                    'border border-transparent',
                    value === type
                      ? 'bg-primary text-primary-foreground shadow-lg glow'
                      : isDisabled
                      ? 'bg-secondary/30 text-muted-foreground cursor-not-allowed opacity-60'
                      : 'bg-secondary/50 hover:bg-secondary text-foreground hover:border-primary/30'
                  )}
                >
                  {config.icon}
                  <span className="text-sm font-medium">{config.label}</span>
                  {isProType && !isPro && (
                    <span className="text-[10px] bg-primary/20 text-primary px-1.5 py-0.5 rounded">
                      Pro
                    </span>
                  )}
                </button>
              );
            })}
          </div>
        </div>
      ))}
    </div>
  );
}
